import type { ReactNode } from 'react';
import { useState } from 'react';
import { motion } from 'motion/react';
import { Mail, ArrowRight } from 'lucide-react';
import { StatusBadge } from '../StatusBadge';
import { MetricCard } from '../MetricCard';
import { DataTable } from '../DataTable';
import { ActionButton, ActionButtonGroup } from '../workspace/ActionButton';
import { DataSourceBadges } from '../workspace/DataSourceBadges';
import { WorkflowActivityTimeline } from '../workspace/WorkflowActivityTimeline';
import { DATA_SOURCES_BY_RESULT } from '../../../data/demoWorkspace';
import {
  ACRYLIC_MATERIAL,
  ACRYLIC_SUPPLIERS,
  ACRYLIC_SUPPLIER_COMPARISON,
  ACRYLIC_SUPPLIER_REPLIES,
  ACRYLIC_PRICING_TIMELINE,
  ACRYLIC_PRICING_EXPLAIN,
} from '../../../data/acrylicDemoData';

interface AcrylicPricingResultProps {
  completedActions: Set<string>;
  onAction: (id: string) => void;
}

export function AcrylicPricingResult({ completedActions, onAction }: AcrylicPricingResultProps) {
  const [showExplain, setShowExplain] = useState(false);

  const handleExplain = () => {
    setShowExplain(true);
    onAction('acrylic-pricing-explain');
  };

  return (
    <div className="space-y-4">
      <div className="flex flex-wrap items-center justify-between gap-2">
        <h3 className="text-lg font-semibold text-white">Acrylic Material Price Update</h3>
        <StatusBadge variant="ai">Generated by AI</StatusBadge>
      </div>

      <WorkflowStep step={1} title="Material needing updated pricing">
        <DataTable
          columns={[
            { key: 'field', header: 'Field', className: 'text-neutral-400' },
            { key: 'value', header: 'Value', className: 'text-white' },
          ]}
          rows={[
            { field: 'Material', value: ACRYLIC_MATERIAL.name },
            { field: 'Specification', value: ACRYLIC_MATERIAL.spec },
            { field: 'Current cost', value: `${ACRYLIC_MATERIAL.previousCost} / sheet` },
            { field: 'Last updated', value: `${ACRYLIC_MATERIAL.lastUpdatedDays} days ago` },
            { field: 'Status', value: <StatusBadge variant="warning">Price update needed</StatusBadge> },
          ]}
          minWidth="360px"
        />
        <AgentMessage>
          The acrylic sheet price has not been updated in {ACRYLIC_MATERIAL.lastUpdatedDays} days. I will
          request updated pricing from approved suppliers.
        </AgentMessage>
      </WorkflowStep>

      <WorkflowStep step={2} title="Pricing requests sent to suppliers">
        <div className="grid gap-3 sm:grid-cols-3">
          {ACRYLIC_SUPPLIERS.map((supplier, i) => (
            <motion.div
              key={supplier}
              initial={{ opacity: 0, x: -12 }}
              animate={{ opacity: 1, x: 0 }}
              transition={{ delay: i * 0.15 }}
              className="rounded-lg border border-sky-500/20 bg-sky-500/5 p-3"
            >
              <div className="mb-2 flex items-center gap-2 text-[10px] font-semibold uppercase tracking-wider text-sky-400">
                <Mail className="h-3 w-3" />
                Outgoing
              </div>
              <p className="text-xs font-medium text-white">To: {supplier}</p>
              <p className="mt-1 text-[10px] text-neutral-500">
                Subject: Updated pricing request — 6mm Clear Cast Acrylic Sheet
              </p>
              <p className="mt-2 text-[10px] leading-relaxed text-neutral-400">
                Hi {supplier}, please confirm latest price and lead time for 6mm Clear Cast Acrylic Sheet
                (1220mm × 2440mm), qty {ACRYLIC_MATERIAL.targetQuantity} sheets…
              </p>
            </motion.div>
          ))}
        </div>
      </WorkflowStep>

      <WorkflowStep step={3} title="Supplier replies received">
        <div className="space-y-2">
          {ACRYLIC_SUPPLIER_REPLIES.map((reply, i) => (
            <motion.div
              key={reply.supplier}
              initial={{ opacity: 0, y: 8 }}
              animate={{ opacity: 1, y: 0 }}
              transition={{ delay: 0.2 + i * 0.2 }}
              className="rounded-lg border border-emerald-500/20 bg-emerald-500/5 px-3 py-2.5"
            >
              <p className="text-[10px] font-semibold uppercase tracking-wider text-emerald-400">
                {reply.supplier} reply
              </p>
              <p className="mt-1 text-xs text-neutral-300">{reply.text}</p>
            </motion.div>
          ))}
        </div>
      </WorkflowStep>

      <WorkflowStep step={4} title="Structured supplier comparison">
        <DataTable
          columns={[
            { key: 'supplier', header: 'Supplier', className: 'text-white' },
            { key: 'unitPrice', header: 'Unit price' },
            { key: 'available', header: 'Available qty' },
            { key: 'leadTime', header: 'Lead time' },
            { key: 'notes', header: 'Notes' },
          ]}
          rows={ACRYLIC_SUPPLIER_COMPARISON.map((row) => ({
            supplier: row.recommended ? (
              <span className="text-violet-300">{row.supplier} ★</span>
            ) : (
              row.supplier
            ),
            unitPrice: row.unitPrice,
            available: row.available,
            leadTime: row.leadTime,
            notes: row.notes,
          }))}
          minWidth="640px"
        />
      </WorkflowStep>

      <WorkflowStep step={5} title="Material cost updated">
        <div className="flex flex-wrap items-center gap-4">
          <div className="rounded-lg border border-neutral-700 bg-neutral-900/60 px-4 py-3">
            <p className="text-[10px] uppercase tracking-wider text-neutral-500">Before</p>
            <p className="mt-1 text-sm text-neutral-400">
              {ACRYLIC_MATERIAL.name}, 6mm: {ACRYLIC_MATERIAL.previousCost} / sheet
            </p>
          </div>
          <ArrowRight className="h-4 w-4 text-violet-400" />
          <motion.div
            animate={{ borderColor: ['rgba(16,185,129,0.3)', 'rgba(16,185,129,0.6)', 'rgba(16,185,129,0.3)'] }}
            transition={{ duration: 2, repeat: Infinity }}
            className="rounded-lg border border-emerald-500/40 bg-emerald-500/10 px-4 py-3"
          >
            <p className="text-[10px] uppercase tracking-wider text-emerald-400">After</p>
            <p className="mt-1 text-sm font-semibold text-white">
              {ACRYLIC_MATERIAL.name}, 6mm: {ACRYLIC_MATERIAL.updatedCost} / sheet
            </p>
            <StatusBadge variant="healthy" className="mt-2">
              Material cost updated
            </StatusBadge>
          </motion.div>
        </div>
        <AgentMessage>
          I updated the material cost to {ACRYLIC_MATERIAL.updatedCost} per sheet based on Supplier B, which
          offers the best balance between price, availability, and lead time. Supplier C is cheaper, but does
          not have enough stock for the required quantity.
        </AgentMessage>
      </WorkflowStep>

      <WorkflowStep step={6} title="Business impact">
        <div className="grid grid-cols-2 gap-2 sm:grid-cols-3 lg:grid-cols-6">
          <MetricCard label="Previous cost" value={ACRYLIC_MATERIAL.previousCost} />
          <MetricCard label="Updated cost" value={ACRYLIC_MATERIAL.updatedCost} valueClassName="text-amber-400" />
          <MetricCard label="Change" value="+$1.30/sheet" valueClassName="text-amber-400" />
          <MetricCard label="Quantity" value={`${ACRYLIC_MATERIAL.targetQuantity} sheets`} />
          <MetricCard label="Total impact" value="+$325" valueClassName="text-amber-400" />
          <MetricCard label="Supplier" value="Supplier B" valueClassName="text-violet-300" />
        </div>
      </WorkflowStep>

      <WorkflowStep step={7} title="Human review">
        <ActionButtonGroup>
          <ActionButton
            id="acrylic-approve-cost"
            label="Approve Material Cost Update"
            variant="primary"
            completed={completedActions.has('acrylic-approve-cost')}
            completedLabel="Approved"
            onClick={onAction}
          />
          <ActionButton
            id="acrylic-lowest-price"
            label="Use Lowest-Price Supplier Instead"
            completed={completedActions.has('acrylic-lowest-price')}
            completedLabel="Supplier C Selected"
            onClick={onAction}
          />
          <ActionButton
            id="acrylic-pricing-explain"
            label="Ask AI to Explain"
            completed={showExplain}
            completedLabel="Explained"
            onClick={handleExplain}
          />
        </ActionButtonGroup>
        {showExplain && (
          <motion.div
            initial={{ opacity: 0, y: 8 }}
            animate={{ opacity: 1, y: 0 }}
            className="mt-3 rounded-lg border border-violet-500/25 bg-violet-500/10 px-4 py-3 text-sm text-neutral-200"
          >
            {ACRYLIC_PRICING_EXPLAIN}
          </motion.div>
        )}
      </WorkflowStep>

      <WorkflowActivityTimeline entries={ACRYLIC_PRICING_TIMELINE} />
      <DataSourceBadges sources={DATA_SOURCES_BY_RESULT['acrylic-pricing']} />
    </div>
  );
}

function WorkflowStep({
  step,
  title,
  children,
}: {
  step: number;
  title: string;
  children: ReactNode;
}) {
  return (
    <motion.div
      initial={{ opacity: 0, y: 10 }}
      animate={{ opacity: 1, y: 0 }}
      transition={{ delay: step * 0.05 }}
      className="rounded-xl border border-neutral-800 bg-[#0a0a0a] p-4"
    >
      <p className="mb-3 text-[10px] font-semibold uppercase tracking-wider text-violet-400">
        Step {step} — {title}
      </p>
      <div className="space-y-3">{children}</div>
    </motion.div>
  );
}

function AgentMessage({ children }: { children: ReactNode }) {
  return (
    <div className="rounded-lg border border-violet-500/20 bg-violet-500/5 px-3 py-2.5 text-sm leading-relaxed text-neutral-200">
      <span className="font-medium text-violet-300">Synpath AI: </span>
      {children}
    </div>
  );
}
